SELECT RB.*, RE.REPLACED_RB_CODES, LNK.LNK_RB_CODES, Q.LNK_QUESTION_IDS, P.LNK_PART_NOS
FROM (SELECT R.*
      FROM (SELECT RB.ID, RB.CODE, RB.TITLE, RB.CONTENT, M.NAME AS MODEL_NAME, ME.NAME AS MODULE_NAME
            FROM T_RB RB
                   JOIN T_MODEL M ON RB.MODEL_ID = M.ID
                   JOIN T_MODULE ME ON RB.MODULE_ID = ME.ID
            WHERE RB.CREATED_DATE >= TO_DATE(:begin, 'yyyy-mm-dd hh24:mi:ss')
              AND RB.CREATED_DATE <= TO_DATE(:end, 'yyyy-mm-dd hh24:mi:ss')) R) RB
       LEFT JOIN (SELECT T_RB_REPLACED.RB_ID, WMSYS.WM_CONCAT(T_RB.CODE) REPLACED_RB_CODES
                  FROM T_RB_REPLACED
                         LEFT JOIN T_RB ON T_RB_REPLACED.REPLACED_RB_ID = T_RB.ID
                  GROUP BY T_RB_REPLACED.RB_ID)RE ON RB.ID = RE.RB_ID
       LEFT JOIN (SELECT T_RB_LNK_BULLETIN.RB_ID, WMSYS.WM_CONCAT(T_RB.CODE) LNK_RB_CODES
                  FROM T_RB_LNK_BULLETIN
                         LEFT JOIN T_RB ON T_RB_LNK_BULLETIN.LNK_RB_ID = T_RB.ID
                  GROUP BY T_RB_LNK_BULLETIN.RB_ID)LNK ON RB.ID = LNK.RB_ID
       LEFT JOIN (SELECT RB_ID, WMSYS.WM_CONCAT(LNK_QUESTION_ID) LNK_QUESTION_IDS FROM T_RB_LNK_QUESTION GROUP BY RB_ID)Q
         ON RB.ID = Q.RB_ID
       LEFT JOIN (SELECT RB_ID, WMSYS.WM_CONCAT(LNK_PART_NO) LNK_PART_NOS FROM T_RB_LNK_PART GROUP BY RB_ID)P
         ON RB.ID = P.RB_ID